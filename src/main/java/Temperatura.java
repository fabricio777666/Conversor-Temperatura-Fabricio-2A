//CLASSE PAI
public class Temperatura {
    //ATRIBUTO
    private double graus;
    //CONSTRUTOR
    public Temperatura(double valorGraus){
        this.graus = valorGraus;
    }
    //GETTER DO GRAUS
    public double getGraus(){
        return graus;
    }
    //SETTEr DO GRAUS
    public void setGraus(double novoGraus){
        this.graus = novoGraus;
    }
    
}
